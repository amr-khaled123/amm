# Equal(==) + Not Equal(!=)
print(100==100) #True
print(100!=100) #False
print(100!=200) #True
print(100==200) #False
print("*"*50)

#more than(>) + Less than(<)
print(100>100) #False
print(100>200) #False
print(100>50) #True
#============================
print(100<100) #False
print(100<200) #True
print(100>50) #False
print("*"*50)

#more than or equal(>=) + less than or equal(<=)
print(100>=100) #True
print(100>=200) #False
print(100>=50) #True
#=================================================
print(100<=100) #True
print(100<=int(99.7)) #False
