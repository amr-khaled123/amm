#========================
#==Assignment Operators==
#========================

x=10
y=20

# +=
x+=y
print(x) #30
print("*"*50)

# -=
x=10
y=20
x-=y
print(x) #-10
print("*"*50)

# *=
x=10
y=20
x*=y
print(x) #200
print("*"*50)

# /=
x=10
y=20
x/=y
print(float(x)) #0.5
print("*"*50)

# **=
x=2
y=3
x**=y
print(x) #8
print("*"*50)

# //=
x=120
y=20
x//=y
print(x) #6